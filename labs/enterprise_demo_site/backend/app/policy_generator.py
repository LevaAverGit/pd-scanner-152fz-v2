def generate_pdf():
    """Generate PDF privacy policy using PyMuPDF."""
    POLICY_DIR.mkdir(parents=True, exist_ok=True)
    pdf_path = POLICY_DIR / "privacy_policy.pdf"
    doc = fitz.open()
    page = doc.new_page(width=595, height=842)  # A4
    rect = fitz.Rect(50, 50, 545, 792)
    page.insert_htmlbox(rect, f"<pre style='font-size:9px;font-family:monospace'>{POLICY_TEXT}</pre>")
    doc.save(str(pdf_path))
    doc.close()
    return pdf_path


